import pymongo


class MongoPipeline(object):

    collection_product = 'products'
    collection_category = 'categories'

    def __init__(self, mongo_uri, mongo_db):
        self.mongo_uri = mongo_uri
        self.mongo_db = mongo_db

    @classmethod
    def from_crawler(cls, crawler):
        return cls(
            mongo_uri=crawler.settings.get('MONGO_URI'),
            mongo_db=crawler.settings.get('MONGO_DATABASE', 'lazada_super')
        )

    def open_spider(self, spider):
        self.client = pymongo.MongoClient(self.mongo_uri)
        self.db = self.client[self.mongo_db]

    def close_spider(self, spider):
        self.client.close()

    def process_item(self, item, spider):
        if item.get('product'):
            self.db[self.collection_product].insert_one(dict(item.get('product')))
        elif item.get('category'):
            self.db[self.collection_category].insert_one(dict(item.get('category')))
        return item
